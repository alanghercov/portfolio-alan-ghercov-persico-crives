# Fonte de referência IQA: CETESB (Companhia Ambiental do Estado de São Paulo)

# Faixas IQA (CETESB):

#   0–40   → Boa

#   41–80  → Moderada

#   81–120 → Ruim

#   121–200→ Muito Ruim

#   201–299→ Péssima



def classificar_iqa(iqa):

    match True:

        case _ if 0 <= iqa <= 40:

            return "Boa"

        case _ if 41 <= iqa <= 80:

            return "Moderada"

        case _ if 81 <= iqa <= 120:

            return "Ruim"

        case _ if 121 <= iqa <= 200:

            return "Muito Ruim"

        case _:

            return "Péssima"





def calcular_nota_conforto(temp, umidade, iqa):

    """

    Fórmula de Conforto Urbano (0 a 10):

    - Temperatura ideal: 22°C. Penaliza desvios.

    - Umidade ideal: entre 50% e 70%. Penaliza extremos.

    - IQA: quanto menor, melhor. Penaliza valores altos.

    """

    # Componente temperatura (0 a 4 pontos): penaliza desvio da zona de conforto

    desvio_temp = abs(temp - 22)

    nota_temp = max(0, 4 - (desvio_temp * 0.3))



    # Componente umidade (0 a 3 pontos): ideal entre 50% e 70%

    if 50 <= umidade <= 70:

        nota_umidade = 3

    elif umidade < 50:

        nota_umidade = max(0, 3 - ((50 - umidade) * 0.06))

    else:

        nota_umidade = max(0, 3 - ((umidade - 70) * 0.06))



    # Componente IQA (0 a 3 pontos): penaliza qualidade ruim do ar

    nota_iqa = max(0, 3 - (iqa * 0.025))



    nota_final = nota_temp + nota_umidade + nota_iqa

    return round(min(nota_final, 10), 2)





