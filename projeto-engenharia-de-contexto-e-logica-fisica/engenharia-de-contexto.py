def simular_evacuacao():

    # Lista dos nós do trajeto (do ponto inicial até a bifurcação)

    nos = [

        "Sala de Aula (Andar 1)",

        "Corredor Principal",

        "Topo da Escada 1",

        "Patamar intermediário",

        "Térreo — Ponto de Decisão",

    ]



    # Estado de cada nó (obstáculos ou facilitadores reais)

    estados = [

        "livre",          # Sala de Aula

        "fumaça leve",    # Corredor

        "livre",          # Topo da Escada 1

        "escada estreita — movimento lento",  # Patamar

        "bifurcação: 3 saídas disponíveis",   # Térreo

    ]



    # As 3 saídas da UNICID com seus estados

    saidas = [

        "Saída 1 — Rua lateral (salas ao lado, passagem direta)",

        "Saída 2 — Avenida principal (espaço amplo, mais segura)",

        "Saída 3 — Lanchonete (corredor apertado, saída pelos fundos)",

    ]



    estados_saidas = [

        "passagem direta para a rua, movimento moderado",

        "ampla, sinalizada, ponto de encontro na avenida",

        "corredor congestionado, mas com saída alternativa pelos fundos",

    ]



    # Inventário do agente

    inventario = ["crachá", "garrafa de água"]

    energia = 100

    posicao = 0

    evacuado = False



    print("=" * 60)

    print("   SIMULADOR DE EVACUAÇÃO — UNICID (1º ANDAR)")

    print("=" * 60)

    print(f"\n🚨 EMERGÊNCIA DETECTADA! Iniciando protocolo de evacuação.")

    print(f"👤 Agente iniciando do nó 0: {nos[0]}")

    print(f"🎒 Inventário: {inventario}")

    print(f"⚡ Energia: {energia}")

    print("-" * 60)



    # Loop principal: agente tenta avançar até chegar ao ponto de decisão

    while posicao < len(nos) and energia > 0 and not evacuado:

        local_atual = nos[posicao]

        estado_atual = estados[posicao]



        print(f"\n📍 Posição atual: [{posicao}] {local_atual}")

        print(f"   Estado: {estado_atual}")



        # Avaliação do nó atual com if aninhado

        if estado_atual == "livre":

            print("   ✅ Caminho livre. Avançando.")

            energia -= 10

            posicao += 1



        elif "fumaça" in estado_atual:

            print("   ⚠️  Há fumaça no corredor!")

            if "garrafa de água" in inventario:

                print("   💧 Usando garrafa de água para umedecer o pano. Atravessando com segurança.")

                inventario.remove("garrafa de água")

                energia -= 20

                posicao += 1

            else:

                print("   😰 Sem recurso para a fumaça. Agente recua um nó e busca rota alternativa.")

                energia -= 30

                if posicao > 0:

                    posicao -= 1



        elif "estreita" in estado_atual or "lento" in estado_atual:

            print("   🪜 Escada estreita — movimento mais lento, porém seguro.")

            energia -= 15

            posicao += 1



        elif "bifurcação" in estado_atual:

            # Chegou ao térreo — hora de escolher a saída

            print("\n" + "=" * 60)

            print("   🔀 PONTO DE DECISÃO: Térreo da UNICID")

            print("   O agente avalia as 3 saídas disponíveis:\n")



            melhor_saida = None

            melhor_score = -1



            for i, saida in enumerate(saidas):

                estado_saida = estados_saidas[i]

                print(f"   [{i+1}] {saida}")

                print(f"        → {estado_saida}")



                # Lógica de pontuação para escolha da melhor saída

                score = 0

                if "ampla" in estado_saida or "segura" in estado_saida:

                    score += 3

                if "direta" in estado_saida:

                    score += 2

                if "congestionado" in estado_saida or "apertado" in estado_saida:

                    score -= 1

                if "alternativa" in estado_saida:

                    score += 1



                print(f"        → Score de segurança calculado: {score}")



                if score > melhor_score:

                    melhor_score = score

                    melhor_saida = i



            print(f"\n   🧠 Agente decide pela: {saidas[melhor_saida]}")

            energia -= 10



            # Executa a saída escolhida

            estado_saida_escolhida = estados_saidas[melhor_saida]



            if "ampla" in estado_saida_escolhida or "segura" in estado_saida_escolhida:

                print("   ✅ Saída segura e organizada. Agente alcança a avenida!")

                evacuado = True



            elif "congestionado" in estado_saida_escolhida:

                print("   ⚠️  Corredor congestionado na lanchonete!")

                if energia > 20:

                    print("   💪 Agente tem energia suficiente para forçar passagem pelos fundos.")

                    energia -= 20

                    evacuado = True

                else:

                    print("   ❌ Energia insuficiente. Agente não consegue avançar pelos fundos.")

                    energia = 0



            else:

                print("   ✅ Passagem direta para a rua. Evacuação concluída pela saída lateral.")

                evacuado = True



            posicao += 1  # sai do loop principal



    # Resultado final

    print("\n" + "=" * 60)

    if evacuado:

        print("🏁 EVACUAÇÃO BEM-SUCEDIDA!")

        print(f"   Energia restante: {energia} / 100")

        print(f"   Inventário final: {inventario if inventario else 'vazio'}")

    elif energia <= 0:

        print("❌ FALHA NA EVACUAÇÃO.")

        print("   O agente ficou sem energia antes de alcançar a saída.")

    else:

        print("⚠️  Simulação encerrada sem conclusão definida.")

    print("=" * 60)





simular_evacuacao()

